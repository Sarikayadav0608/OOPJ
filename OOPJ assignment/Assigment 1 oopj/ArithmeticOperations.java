import java.util.Scanner;
class ArithmeticOperations{
         public static void main(String[] args){
		 Scanner sc=new Scanner(System.in);
		 System.out.println("Enter two no. : ");
		 int no1=sc.nextInt();
		 int no2=sc.nextInt();
		 System.out.println("no1 = "+no1);
		 System.out.println("no2 = "+no2);
		 System.out.println("The Add of no1 and no2 is = "+(no1+no2));
		 System.out.println("The Sub of no1 and no2 is = "+(no1-no2));
		 System.out.println("The Mul of no1 and no2 is = "+(no1*no2));
		 System.out.println("The Div of no1 and no2 is = "+(no1/no2));
		 System.out.println("The Mod of no1 and no2 is = "+(no1%no2));
		 
		 }
    }